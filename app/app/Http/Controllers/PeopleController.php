    public function profileUpdate(Request $request)
    {
        $this->validate(request(), [
            'password' => 'required|string|min:6|confirmed',
        ]);

        $user = Auth::user();
        
        $user->password = bcrypt($request->get('password'));

        $user->save();

        Session::flash('save','Contraseña editada con éxito.');
        return back();
    }

